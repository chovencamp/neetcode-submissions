def get_min(a: int, b: int) -> int:
    if a < b:
        return a #check if a is less than b 
    else:
        return b #since we only have two paramaters this will catch all the other conditions 



    
# don't modify code below this line
print(get_min(10, 11))
print(get_min(5, -7))
print(get_min(20, 20))
